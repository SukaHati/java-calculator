    private void jButton15ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton15ActionPerformed
        // This is Button =
        // Calculate the number
        if(arrValue != 0){
            arrayOps[arrValue] = "=";
            int i = 0;
            while(true){
                if(arrayOps[i].equals("×")){
                    numValue[i] = numValue[i] * numValue[i + 1];
                    calculateValue(i);
                } else if(arrayOps[i].equals("÷")){
                    numValue[i] = numValue[i] / numValue[i + 1];
                    calculateValue(i);
                }else{
                    i++;
                }
                if(arrayOps[i].equals("=")){
                    i = 0;
                    break;
                }
            }
            while(true){
                if(arrayOps[i].equals("+")){
                    numValue[i] = numValue[i] + numValue[i + 1];
                    calculateValue(i);
                } else if(arrayOps[i].equals("-")){
                    numValue[i] = numValue[i] - numValue[i + 1];
                    calculateValue(i);
                }else{
                    i++;
                }
                if(arrayOps[0].equals("=")){
                    i = 0;
                    break;
                }
            }
        }
        arrValue = 0;
        dot = false;
        numDisplay = String.valueOf(numValue[0]);
        jLabel1.setText(numDisplay);
    }//GEN-LAST:event_jButton15ActionPerformed

