    private void calculateValue(int num1){
        arrayOps[num1] = arrayOps[num1 + 1];
        for(int j = 1; j <= arrValue; j++){
            numValue[num1 + j] = numValue[num1 + j + 1];
            arrayOps[num1 + j] = arrayOps[num1 + j + 1];
        }
    }
    
    //Insert number from buttons here
    private void numButtonsExecution(int numButton){
        //Recurring button execution
        if(!dot){ //If button . is not pressed
            if(numValue[arrValue] == 0){
                numValue[arrValue] = numButton;
            } else if(numValue[arrValue] > 0){
                numValue[arrValue] = numValue[arrValue] * 10 + numButton;
            }
            if(numDisplay.equals("0")){ //If numDisplay value is null, replace the value
                numDisplay = String.valueOf(numButton);
            } else{ //Combine the number
                numDisplay = numDisplay + String.valueOf(numButton);
            }
        } else{ //If button . is pressed
            if(numButton == 0){
                valueNegativePower++;
            } else{
                numValue[arrValue] = numValue[arrValue] + (numButton / (float)Math.pow(10, valueNegativePower)); //(float).variable convert variable type to float
            }
            numDisplay = numDisplay + String.valueOf(numButton);
        }
        jLabel1.setText(numDisplay);
    }
    
    // Place mathematics operation symbol here
    private void buttonInsertSymbol(String symOps){
        numDisplay = numDisplay + symOps;
        arrayOps[arrValue] = symOps;
        arrValue++;
        dot = false;
        jLabel1.setText(numDisplay);
    }
